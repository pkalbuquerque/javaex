package POO.introducao.lista_exercicios01.exercicio05;

public class DiadaSemana {
    private int ano;
    private int mes;
    private int dia;
    
    public int getAno() {
        return ano;
    }
    public void setAno(int ano) {
        this.ano = ano;
    }
    public int getMes() {
        return mes;
    }
    public void setMes(int mes) {
        this.mes = mes;
    }
    public int getDia() {
        return dia;
    }
    public void setDia(int dia) {
        this.dia = dia;
    }

    
    
}
