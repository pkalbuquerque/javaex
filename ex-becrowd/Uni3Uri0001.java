import java.util.Scanner;

public class Uni3Uri0001 {

public static void main(String[] args) {
    Scanner teclado = new Scanner(System.in);

    //Início 
    System.out.println("Informe um número inteiro para x");
    int num1 = teclado.nextInt();
    System.out.println("Informe um segundo número inteiro para x");
    int num2 = teclado.nextInt();

    //Processo
    int total = num1 + num2; 

    //Saída
    System.out.println("Valor de X:" + (total));
    teclado.close();








}
    
}
